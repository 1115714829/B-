using System;

namespace 第十二课_B站第50课for循环练习
{
    class Program
    {
        static void Main(string[] args)
        {
            //1.for循环的正序输出和倒序输出

            //请打印1到10的数字
            for (int i = 1; i <= 10; i++)
            {
                Console.WriteLine(i);
            }

            //打印10到1的数字
            for (int i = 10; i >= 1; i--)
            {
                Console.WriteLine(i);
            }
            for (int i = 10; i >= 1; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
